extern void delay_xus(uint32_t n);
extern void delay_xms(uint32_t n);

#define SCL_W	PDout(9)
#define SDA_W	PEout(15)
#define SDA_R	PEin(15)

static GPIO_InitTypeDef GPIO_InitStructure;

//OLED的显存
//存放格式如下.
//[0]0 1 2 3 ... 127	
//[1]0 1 2 3 ... 127	
//[2]0 1 2 3 ... 127	
//[3]0 1 2 3 ... 127	
//[4]0 1 2 3 ... 127	
//[5]0 1 2 3 ... 127	
//[6]0 1 2 3 ... 127	
//[7]0 1 2 3 ... 127 			   
void sda_pin_mode(GPIOMode_TypeDef pin_mode)
{

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;		    //第1根引脚
	GPIO_InitStructure.GPIO_Mode= pin_mode;	            //输出/输入模式
	GPIO_InitStructure.GPIO_OType = GPIO_OType_OD;	    //推挽输出，增加输出电流能力。
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;  //高速响应
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;	//没有使能上下拉电阻

	GPIO_Init(GPIOB,&GPIO_InitStructure);
}

/**********************************************
//IIC Start
**********************************************/
void IIC_Start(void)
{
	//保证SDA引脚为输出模式
	sda_pin_mode(GPIO_Mode_OUT);
	
	SCL_W=1;
	SDA_W=1;
	delay_xus(5);

	SDA_W=0;
	delay_xus(5);
	
	
	SCL_W=0;
	delay_xus(5);		//钳住I2C总线

}

